    private void upActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upActionPerformed
        // seçimleri yukarı taşır
        int[] sin = list.getSelectedIndices();
        int i = 0;
        for (String j : list.getSelectedValuesList()) {
            if (j != null) {
                if (sin[i] > 0) {
                    File j1 = ad[sin[i]];
                    String temp = d[sin[i] - 1];
                    File temp1 = ad[sin[i] - 1];
                    d[sin[i] - 1] = j;
                    ad[sin[i] - 1] = j1;
                    d[sin[i]] = temp;
                    ad[sin[i]] = temp1;
                    list.setListData(d);
                    list.setSelectedIndex(sin[i] - 1);
                }
            }
            sin[i]--;
            i++;
        }
        list.setSelectedIndices(sin);
    }//GEN-LAST:event_upActionPerformed

    private void downActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downActionPerformed
        //seçimleri aşağı haraket ettirir
        int[] sin = list.getSelectedIndices();
        int i = sin.length - 1;
        List<String> düzd = list.getSelectedValuesList();
        String[] ters = new String[düzd.size()];
        düzd.toArray(ters);
        for (int q = ters.length - 1; q >= 0; q--) {
            if (ters[q] != null) {
                if (sin[i] < d.length - 1) {
                    File j1 = ad[sin[i]];
                    String temp = d[sin[i] + 1];
                    File temp1 = ad[sin[i] + 1];
                    d[sin[i] + 1] = ters[q];
                    ad[sin[i] + 1] = j1;
                    d[sin[i]] = temp;
                    ad[sin[i]] = temp1;
                    list.setListData(d);
                    list.setSelectedIndex(sin[i] + 1);
                }
            }
            sin[i]++;
            i--;
        }
        list.setSelectedIndices(sin);
    }//GEN-LAST:event_downActionPerformed

