    static String sss(File s) throws IOException {
        Path f = s.toPath();
        SimpleDateFormat sf = new SimpleDateFormat("MM:dd:HH:mm:ss");
        BasicFileAttributes attr = Files.readAttributes(f, BasicFileAttributes.class);
        String d = sf.format(attr.creationTime().toMillis());
        return d;
    }

    static int[] tarc(File a[]) throws IOException {
        int[] ii = new int[a.length];
        for (int i = 0; i < a.length; i++) {
            String s[] = sss(a[i]).split(":");
            String ss = "";
            for (int j = 0; j < s.length; j++) {
                ss += s[j];
            }
            ii[i] = Integer.parseInt(ss);
        }

        return ii;
    }

    static int[] tarm(File a[]) {
        SimpleDateFormat sdf = new SimpleDateFormat("MMddHHmmss");
        int[] ii = new int[a.length];
        for (int i = 0; i < a.length; i++) {
            ii[i] = Integer.parseInt(sdf.format(a[i].lastModified()));
        }
        return ii;
    }

    void sıralama() throws IOException {
        int[] fg = null;
        if (data1.isSelected()) {
            fg = tarc(ad);
        } else if (data2.isSelected()) {
            fg = tarm(ad);
        }

        for (int i = 0; i < ad.length - 1; i++) {
            int minind = i;
            for (int j = i + 1; j < ad.length; j++) {
                if (fg[j] < fg[minind]) {
                    minind = j;
                }
            }
            if (minind != i) {
                File ara = ad[minind];
                String ara1 = d[minind];
                int ara2 = fg[minind];
                ad[minind] = ad[i];
                d[minind] = d[i];
                fg[minind] = fg[i];
                ad[i] = ara;
                d[i] = ara1;
                fg[i] = ara2;
            }
        }

        list.setListData(d);
    }

